    <h2 id="permalinkpattern" tabindex="-1">
      <a class="header-anchor" href="#permalinkpattern" aria-hidden="true">#</a>
      permalinkPattern
    </h2>
    <ul>
      <li>
        <p>Type: <code v-pre>string | null</code></p>
      </li>
      <li>
        <p>Details:</p>
        <p>Pattern to generate permalink for the page.</p>
        <p>
          This will override the <code v-pre>permalinkPattern</code> option in
          your site config.
        </p>
        <p>
          This won't take effect if the <code v-pre>permalink</code> frontmatter
          has been set.
        </p>
      </li>
      <li>
        <p>Usage:</p>
        <table>
          <thead>
            <tr>
              <th>Pattern</th>
              <th>Description</th>
            </tr>
          </thead>
          <tbody>
            <tr>
              <td><code v-pre>:year</code></td>
              <td>Year part of created date</td>
            </tr>
            <tr>
              <td><code v-pre>:month</code></td>
              <td>Month part of created date</td>
            </tr>
            <tr>
              <td><code v-pre>:day</code></td>
              <td>Day part of created date</td>
            </tr>
            <tr>
              <td><code v-pre>:slug</code></td>
              <td>Slug of page filename</td>
            </tr>
            <tr>
              <td><code v-pre>:raw</code></td>
              <td>Raw route path</td>
            </tr>
          </tbody>
        </table>
        <p>
          The <code v-pre>:year</code>, <code v-pre>:month</code> and
          <code v-pre>:day</code> patterns are resolved according to the
          following priority:
        </p>
        <ul>
          <li>The <code v-pre>date</code> frontmatter.</li>
          <li>
            The filename that matches the date pattern
            <code v-pre>yyyy-MM-dd-foobar.md</code> or
            <code v-pre>yyyy-MM-foobar.md</code>.
          </li>
          <li>
            The dirname that matches the date pattern
            <code v-pre>yyyy/MM/dd/foobar.md</code> or
            <code v-pre>yyyy/MM/foobar.md</code>.
          </li>
          <li>Fallback to <code v-pre>0000-00-00</code>.</li>
        </ul>
      </li>
      <li>
        <p>Example 1:</p>
        <p>The page filename is <code v-pre>foo-bar.md</code>.</p>
        <p>The page frontmatter is:</p>
      </li>
    </ul>
    <div class="language-markdown ext-md line-numbers-mode">
      <pre
        v-pre
        class="language-markdown"
      ><code><span class="token front-matter-block"><span class="token punctuation">---</span>
<span class="token front-matter yaml language-yaml"><span class="token key atrule">date</span><span class="token punctuation">:</span> <span class="token datetime number">2021-01-03</span>
<span class="token key atrule">permalinkPattern</span><span class="token punctuation">:</span> <span class="token punctuation">:</span>year/<span class="token punctuation">:</span>month/<span class="token punctuation">:</span>day/<span class="token punctuation">:</span>slug.html</span>
<span class="token punctuation">---</span></span>
</code></pre>
      <div class="line-numbers" aria-hidden="true">
        <div class="line-number"></div>
        <div class="line-number"></div>
        <div class="line-number"></div>
        <div class="line-number"></div>
      </div>
    </div>
    <p>
      Then the permalink of the page would be
      <code v-pre>2021/01/03/foo-bar.html</code>.
    </p>
    <ul>
      <li>
        <p>Example 2:</p>
        <p>The page filename is <code v-pre>2021-01-03-bar-baz.md</code>.</p>
        <p>The page frontmatter is:</p>
      </li>
    </ul>
    <div class="language-markdown ext-md line-numbers-mode">
