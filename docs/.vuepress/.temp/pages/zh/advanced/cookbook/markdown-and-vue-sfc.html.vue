      Markdown 与 Vue SFC
    </h1>
    <p>
      每一个 Markdown 文件，首先都会编译为 HTML ，然后转换为一个 Vue 单文件组件
      (SFC) 。换句话说，你可以像写 Vue SFC 一样来写 Markdown 文件：
    </p>
    <ul>
      <li>
        <code v-pre>&lt;script&gt;</code> 和
        <code v-pre>&lt;style&gt;</code> 标签会直接被当作 Vue SFC
        中的标签。换句话说，它们是从
        <code v-pre>&lt;template&gt;</code> 标签中提升到了 SFC 的顶层。
      </li>
      <li>
        所有 <code v-pre>&lt;script&gt;</code> 和
        <code v-pre>&lt;style&gt;</code> 标签的以外的内容，会先被编译为 HTML
        ，然后被当作 Vue SFC 的 <code v-pre>&lt;template&gt;</code> 标签。
      </li>
    </ul>
    <div class="custom-container warning">
      <p class="custom-container-title">WARNING</p>
      <p>
        由于 Vue 单文件组件只能包含一个
